static void		patch_payload(t_packer *pack, t_bdata *data)
{
	int off = 63;
    Elf64_Addr const entrypoint = data->original_entrypoint - (data->segment.p_vaddr + data->segment.p_filesz) - PAYLOAD_SIZE;
	Elf64_Off const entry_encrypt = entrypoint + (PAYLOAD_SIZE - 100 - 4);
	Elf64_Off const limit = data->section.sh_size - (data->original_entrypoint - data->section.sh_addr);

	ft_memcpy(&payload[100], &entry_encrypt, sizeof(int));
	ft_memcpy(&payload[105], &limit, sizeof(int));

	for (register unsigned char i = 0; i < KEY_SIZE; i++)
	{
		payload[off] = pack->key[i];
		off += 4;
	}
	ft_memcpy(&payload[PAYLOAD_SIZE - sizeof(int)], &entrypoint, sizeof(int));
}

static void	insert_payload(t_packer *pack, t_packer *infected, t_bdata *bdata)
{
	size_t	offset = 0;
	char	*src = pack->map;
	char	*dst = infected->map;

	Elf64_Off encrypt_beg = bdata->section.sh_offset + (bdata->original_entrypoint - bdata->section.sh_addr);
	Elf64_Off encrypt_end = bdata->section.sh_offset + bdata->section.sh_size;
	Elf64_Off payload_beg = bdata->segment.p_offset + bdata->segment.p_filesz;
	Elf64_Off payload_end = bdata->segment.p_offset + bdata->segment.p_filesz + PAGESIZE;
	Elf64_Off end_of_file = pack->size + PAGESIZE;

	while (offset < encrypt_beg)
	{
		*dst++ = *src++;
		offset++;
	}

	while (offset < encrypt_end)
	{
		*dst++ = *src++;
		offset++;
	}

	while (offset < payload_beg)
	{
		*dst++ = *src++;
		offset++;
	}

	while (offset < payload_end)
	{
		*dst++ = 0;
		offset++;
	}

	while (offset < end_of_file)
	{
		*dst++ = *src++;
		offset++;
	}

	ft_memcpy(infected->map + payload_beg, payload, PAYLOAD_SIZE);
	_rc4((uint8_t*)pack->key, KEY_SIZE, infected->map + encrypt_beg, encrypt_end - encrypt_beg);

	if (write(infected->fd, infected->map, end_of_file) < (ssize_t)end_of_file)
		dprintf(STDERR_FILENO, "Error: can't write into packed binary. abort.");
}

void	create_infected(t_packer *pack, t_bdata *data)
{
	t_packer infected;

	infected.fd = open(OUTPUT_NAME, O_RDWR | O_CREAT | O_TRUNC, 0700);
	if (infected.fd < 0)
	{
		perror("open");
		return ;
	}
	infected.map = mmap(0, pack->size + PAGESIZE, PROT_READ | PROT_WRITE, MAP_PRIVATE | MAP_ANON, -1, 0);
	if (infected.map == MAP_FAILED)
	{
		perror("mmap");
		close(infected.fd);
		return ;
	}

	patch_payload(pack, data);
	insert_payload(pack, &infected, data);

	close(infected.fd);
	munmap(infected.map, pack->size + PAGESIZE);
}
